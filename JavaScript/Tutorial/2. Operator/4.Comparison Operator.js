//Equality Operator 
let val1=5;
let val2='5';
console.log(val1 == 5);
console.log(val2 == 5);        
console.log(val1 == val1);
console.log(0 == false);   
console.log(0 == null);

//Inequality Operator
let val3 = 5;
let val4 = '5';
console.log(val3 != 6);
console.log(val4 != '5');        
console.log(val3 != val4);
console.log(0 != false);   
console.log(0 != null);

//Strict equality Operator
let val11 = 5;
let val12 = '5';
console.log(val11 === 6);
console.log(val12 === '5');        
console.log(val11 === val12);
console.log(0 === false);   
console.log(0 === null);

//Strict inequality Operator
let val21 = 5;
let val22 = '5';
console.log(val21 !== 6);
console.log(val22 !== '5');        
console.log(val21 !== val22);
console.log(0 !== false);   
console.log(0 !== null);

//Greater than Operator
let val31 = 5;
let val32 = "5";
console.log(val31 > 0);
console.log(val32 > "10");        
console.log(val31 > "10");
console.log(val32 > 0);

//Greater than or equal Operator 
let val41 = 5;
let val42 = "5";
console.log(val41 >= 5);
console.log(val42 >= "15");        
console.log(val41 >= "5");
console.log(val42 >= 15);

//Less than Operator
let val51 = 5;
let val52 = "5";
console.log(val51 < 15);
console.log(val52 < "0");        
console.log(val51 < "0");
console.log(val52 < 15);

//Less than or equal Operator
let val61 = 5;
let val62='5';
console.log(val61 <= 15);
console.log(val62 <= "0");        
console.log(val61 <= "0");
console.log(val62 <= 15);