
// Map Array 
// Map Array and Object are all iterable but they are not arrays. So we cannot use for of loop on them. We can use for in loop to iterate over the keys of an object or an array.

const myObject = {
    js: 'javascript',
    cpp: 'C++',
    rb: "ruby",
    swift: "swift by apple"
}

for (const key in myObject) {
    console.log(`${key} shortcut is for ${myObject[key]}`);
}

const programming = ["js", "rb", "py", "java", "cpp"]

for (const key in programming) {
    console.log(programming[key]);
}

